function sha256(data) {
    return crypto.createHash("sha512").update(data, "binary").digest("base64");        
}

exports.sha256 = sha256;

async function getDevices(){
    var result = await queryAllDevices();
    result =  JSON.parse(result);
     //[{"Key":"el id" , "Record":{"issue_date" : "tal" , "issuer":"Org1MSP" , "state":0 }]        
     var devices = [];
     result.forEach(element => {
         let d = {
             'deviceId' :  element.Key,
             'issue_date': element.Record.issue_date , 
             'issuer' : element.Record.issuer ,
             'state' : element.Record.state ,
             'hash_generated' : element.Record.hash_generated  ,
             'ip' : element.Record.ip ,
             'mac' : element.Record.mac ,
             'modelo' :  element.Record.modelo
         }
         devices.push(d);
     });  
     return devices;
}

exports.getDevices = getDevices;